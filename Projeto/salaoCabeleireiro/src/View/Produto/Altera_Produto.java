    public void tremeTela(){
        Point p = this.getLocation();
        Altera_Produto altera_produto = this;
        new Thread(){
            @Override
            public void run() {
                try{
                    for (int i = 0; i < 4; i++) {
                    altera_produto.setLocation(p.x - 10, p.y);
                    sleep(20);
                    altera_produto.setLocation(p.x + 10, p.y);
                    sleep(20);
                    }
                    altera_produto.setLocation(p.x, p.y);
                }catch(InterruptedException ex){
                    Logger.getLogger(Altera_Produto.class.getName()).log(Level.ALL.SEVERE, null, ex);
                }
                
            }
            
        }.start();
    }
    
